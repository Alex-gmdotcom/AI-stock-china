# ============================================================
# 部署逻辑
# ============================================================

def main() -> int:
    print("=" * 70)
    print(f"Phase 1 部署脚本  ({datetime.now():%Y-%m-%d %H:%M:%S})")
    print(f"仓库根目录: {REPO_ROOT}")
    print("=" * 70)

    # 简单 sanity check：确认当前目录是仓库根
    if not (REPO_ROOT / "src").is_dir():
        print(f"\n[FATAL] 在 {REPO_ROOT} 下找不到 src/ 子目录。")
        print(f"        请把本脚本放到仓库根 (含 src/ 的那一层) 再跑。")
        return 1

    # 1. 备份现有文件
    backup_dir = REPO_ROOT / "phase1_backup" / datetime.now().strftime("%Y%m%d_%H%M%S")
    print(f"\n[Step 1/4] 备份现有文件到: {backup_dir.relative_to(REPO_ROOT)}")
    backup_count = 0
    for target_rel in FILES_B64:
        target_abs = REPO_ROOT / target_rel
        if target_abs.exists():
            backup_path = backup_dir / target_rel
            backup_path.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(target_abs, backup_path)
            backup_count += 1
            print(f"  backed up  {target_rel}")
        else:
            print(f"  (new)      {target_rel}")
    print(f"  -> 共备份 {backup_count} 个文件")

    # 2. 写入新文件（强制覆盖）
    print(f"\n[Step 2/4] 写入 Phase 1 文件")
    for target_rel, b64_bytes in FILES_B64.items():
        target_abs = REPO_ROOT / target_rel
        target_abs.parent.mkdir(parents=True, exist_ok=True)
        # 确保父目录有 __init__.py（Python 包必须）
        pkg_init = target_abs.parent / "__init__.py"
        if not pkg_init.exists() and pkg_init.name == "__init__.py":
            pass  # 自己就是 __init__.py
        content = base64.b64decode(b64_bytes)
        target_abs.write_bytes(content)
        print(f"  wrote      {target_rel}  ({len(content)} bytes)")

    # 确保 tools/__init__.py 和 strategy/__init__.py 存在
    for pkg in ("src/tools", "src/strategy"):
        init = REPO_ROOT / pkg / "__init__.py"
        if not init.exists():
            init.write_text("", encoding="utf-8")
            print(f"  created    {pkg}/__init__.py (empty)")

    # 3. 清 __pycache__
    print(f"\n[Step 3/4] 清理 __pycache__")
    pycache_count = 0
    for pyc_dir in REPO_ROOT.rglob("__pycache__"):
        if "phase1_backup" in pyc_dir.parts:
            continue
        shutil.rmtree(pyc_dir, ignore_errors=True)
        pycache_count += 1
        print(f"  removed    {pyc_dir.relative_to(REPO_ROOT)}")
    if pycache_count == 0:
        print(f"  (no __pycache__ found)")

    # 4. 跑 5 个自测
    print(f"\n[Step 4/4] 跑 5 个自测")
    tests = [
        ("markets.proxy",             "src.markets.proxy"),
        ("tools.data_fallback",       "src.tools.data_fallback"),
        ("strategy.three_categories", "src.strategy.three_categories"),
        ("llm_text",                  "src.llm_text"),
        ("boot (横幅)",               "src.boot"),
    ]
    results = []
    for label, module in tests:
        print(f"\n  --- Test: {label} ---")
        try:
            proc = subprocess.run(
                [sys.executable, "-m", module],
                capture_output=True,
                text=True,
                timeout=30,
                cwd=str(REPO_ROOT),
                encoding="utf-8",
                errors="replace",
            )
            tail = (proc.stdout or "")[-600:]
            print(tail)
            ok = proc.returncode == 0 and (
                "ALL TESTS PASSED" in proc.stdout
                or "Self-test done" in proc.stdout
                or "test] done" in proc.stdout
            )
            results.append((label, ok, proc.returncode))
        except Exception as e:
            print(f"  EXCEPTION: {e}")
            results.append((label, False, -1))

    # 汇总
    print(f"\n" + "=" * 70)
    print(f"汇总")
    print(f"=" * 70)
    all_ok = True
    for label, ok, rc in results:
        status = "OK" if ok else f"FAIL (rc={rc})"
        print(f"  {status:<14s} {label}")
        if not ok:
            all_ok = False

    print(f"\n备份目录: {backup_dir.relative_to(REPO_ROOT)}")
    if all_ok:
        print(f"\n[SUCCESS] Phase 1 部署完成，全部自测通过。")
        return 0
    else:
        print(f"\n[PARTIAL] 部分自测未通过，把上面输出贴回 Claude 看。")
        return 2


if __name__ == "__main__":
    sys.exit(main())
