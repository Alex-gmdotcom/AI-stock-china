def main() -> int:
    cwd = Path.cwd()
    print("=" * 76)
    print("Phase 3 部署 — agent 兼容接口 (AKShare 真实现)")
    print("=" * 76)
    print(f"CWD: {cwd}")

    src_dir = cwd / "src"
    if not src_dir.is_dir():
        print(f"[ERROR] {src_dir} 不存在 — 请在仓库根目录运行")
        return 1

    # 备份
    backup_dir = cwd / "phase3_backup" / datetime.now().strftime("%Y%m%d_%H%M%S")
    backup_dir.mkdir(parents=True, exist_ok=True)
    print(f"[BACKUP DIR] {backup_dir}")

    for rel in ["src/tools/api_china.py", "src/tools/line_items_china.py"]:
        src_path = cwd / rel
        if src_path.exists():
            dest = backup_dir / rel
            dest.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(src_path, dest)
            print(f"  BACKUP {rel}")
        else:
            print(f"  (新建) {rel}")

    # 解压 payload
    print("\n[WRITE] 解压 base64 payload...")
    try:
        zip_data = base64.b64decode("".join(PAYLOAD_B64.split()))
    except Exception as exc:
        print(f"[ERROR] base64 解码失败: {exc}")
        return 1

    written = 0
    with zipfile.ZipFile(io.BytesIO(zip_data)) as zf:
        for member in zf.namelist():
            if member.endswith("/"):
                continue
            target = cwd / member
            target.parent.mkdir(parents=True, exist_ok=True)
            with zf.open(member) as src_f, open(target, "wb") as dst_f:
                shutil.copyfileobj(src_f, dst_f)
            print(f"  WRITE {member}  ({target.stat().st_size:,} bytes)")
            written += 1

    # 清 __pycache__
    cleaned = 0
    for pyc in src_dir.rglob("__pycache__"):
        if pyc.is_dir():
            shutil.rmtree(pyc, ignore_errors=True)
            cleaned += 1
    print(f"\n[CLEAN] 删除 {cleaned} 个 __pycache__")

    # 跑 self-test
    print("-" * 76)
    print("[TEST] 跑 self-test ...")
    print("-" * 76)

    env = os.environ.copy()
    env["PYTHONPATH"] = str(cwd) + os.pathsep + env.get("PYTHONPATH", "")

    test_modules = [
        ("src.tools.api_china", "v1.0.1 + v1.0.2 + v1.0.3 (28 项)"),
        ("src.tools.line_items_china", "v1.0.0 (4 项)"),
    ]

    all_ok = True
    for module_name, label in test_modules:
        result = subprocess.run(
            [sys.executable, "-m", module_name],
            env=env, cwd=str(cwd),
            capture_output=True, text=True, timeout=120,
        )
        ok = (result.returncode == 0)
        symbol = "✓" if ok else "✗"
        tail = result.stdout.strip().splitlines()[-1] if result.stdout.strip() else "(no output)"
        print(f"  {symbol} {module_name:<35s} {label}")
        print(f"      {tail[:100]}")
        if not ok:
            all_ok = False
            if result.stderr:
                print(f"      STDERR: {result.stderr[:400]}")

    print("=" * 76)
    if all_ok:
        print(f"[SUCCESS] Phase 3 部署完成")
        print(f"  写入 {written} 个文件")
        print(f"  备份: {backup_dir}")
        print()
        print("下一步:")
        print("  1. 重启 web_app:")
        print("     poetry run python src/web_app.py")
        print()
        print("  2. 浏览器 http://localhost:8000 → 选 1-2 只票 (e.g. 600519)")
        print("     → 选 'warren_buffett' agent → 点 '开始分析'")
        print("     预期: 不再 500. 返回真分析结果 (基于 AKShare 数据).")
        print()
        print("  3. 如某个 AKShare 字段映射错 (字段为 None 但应该有值), 把")
        print("     analyze 返回的 JSON 贴回来, 我据反馈精调字段名映射.")
        return 0
    else:
        print(f"[FAIL] self-test 未全过")
        print(f"\n回滚:")
        print(f"  copy /Y /Y \"{backup_dir}\\src\\tools\\api_china.py\" \"{cwd}\\src\\tools\\api_china.py\"")
        return 1


if __name__ == "__main__":
    sys.exit(main())
