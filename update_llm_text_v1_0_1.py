def main():
    print(f"升级 {TARGET.relative_to(REPO_ROOT)} → v1.0.1")
    if not TARGET.exists():
        print(f"[FATAL] {TARGET} 不存在；先跑 deploy_phase1.py")
        return 1
    
    # 备份
    bak = REPO_ROOT / "phase1_backup" / f"llm_text_{datetime.now():%Y%m%d_%H%M%S}.py.bak"
    bak.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(TARGET, bak)
    print(f"  备份: {bak.relative_to(REPO_ROOT)}")
    
    # 写新版本
    content = base64.b64decode(LLM_TEXT_B64)
    TARGET.write_bytes(content)
    print(f"  写入: {len(content)} bytes")
    
    # 清缓存
    pyc = REPO_ROOT / "src" / "__pycache__"
    if pyc.exists():
        shutil.rmtree(pyc, ignore_errors=True)
        print(f"  清: src/__pycache__")
    
    # 验证
    import importlib.util
    spec = importlib.util.spec_from_file_location("llm_text_v2", TARGET)
    mod = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(mod)
    ds = mod.PROVIDERS["deepseek"]
    print(f"\n  验证：")
    print(f"    version = {mod.__version__}")
    print(f"    default = {ds['default_model']}")
    print(f"    token_limit = {ds['token_limit']:,}")
    print(f"    models = {ds['models']}")
    
    if mod.__version__ == "v1.0.1" and ds["default_model"] == "deepseek-v4-flash":
        print(f"\n[SUCCESS] llm_text.py 升级到 v1.0.1 完成")
        return 0
    else:
        print(f"\n[FAIL] 验证失败，请检查")
        return 1


if __name__ == "__main__":
    import sys
    sys.exit(main())
